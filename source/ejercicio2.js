// Función para mostrar los primeros 10 números pares  
const mostrarNumerosPares = () => {  
    for (let i = 0; i < 10; i++) {  
        console.log(i * 2); // Los números pares son el doble de i (0, 2, 4, ..., 18)  
    }  
};  

// Llamar a la función para mostrar los números  
mostrarNumerosPares();  